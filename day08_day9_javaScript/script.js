function result1(){
    let tMark=document.querySelector(".tMark").value;
    let pMark=document.querySelector(".pMark").value;
    let total=Number(tMark) + Number(pMark);
    document.querySelector(".totalMark").value=total;

    if(total>=80 && total<=100){
        document.querySelector(".grade").value="A+";
        document.querySelector(".point").value="5.0";
    }
    else if(total>=70 && total<=79){
        document.querySelector(".grade").value="A";
        document.querySelector(".point").value="4.0";
    }
    else if(total>=60 && total<=69){
        document.querySelector(".grade").value="A-";
        document.querySelector(".point").value="3.5";
    }
    else if(total>=50 && total<=59){
        document.querySelector(".grade").value="B";
        document.querySelector(".point").value="3.0";
    }
    else if(total>=40 && total<=40){
        document.querySelector(".grade").value="c";
        document.querySelector(".point").value="2.0";
    }
    else if(total>=33 && total<=39){
        document.querySelector(".grade").value="D";
        document.querySelector(".point").value="1.0";
    }
    else if(total>=0 && total<=32){
        document.querySelector(".grade").value="F";
        document.querySelector(".point").value="0.0";
    }
    else{
      alert("Invalid Input")
    }
}

